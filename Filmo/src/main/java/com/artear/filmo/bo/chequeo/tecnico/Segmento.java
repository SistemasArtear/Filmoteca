package com.artear.filmo.bo.chequeo.tecnico;

public class Segmento {

	private Integer nroFicha;
	private Integer numero;
	private String titulo;
	private Integer duracion;
	private String chequeo;

	public Integer getNroFicha() {
		return nroFicha;
	}

	public void setNroFicha(Integer nroFicha) {
		this.nroFicha = nroFicha;
	}

	public Integer getNumero() {
		return numero;
	}

	public void setNumero(Integer numero) {
		this.numero = numero;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public Integer getDuracion() {
		return duracion;
	}

	public void setDuracion(Integer duracion) {
		this.duracion = duracion;
	}

	public String getChequeo() {
		return chequeo;
	}

	public void setChequeo(String chequeo) {
		this.chequeo = chequeo;
	}

}
