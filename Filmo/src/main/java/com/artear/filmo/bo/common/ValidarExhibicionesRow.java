package com.artear.filmo.bo.common;

public class ValidarExhibicionesRow {
	
	private Integer numeroCapitulo;
	private Integer numeroParte;
	private String clave;
	private String titulo;
	public Integer getNumeroCapitulo() {
		return numeroCapitulo;
	}
	public void setNumeroCapitulo(Integer numeroCapitulo) {
		this.numeroCapitulo = numeroCapitulo;
	}
	public Integer getNumeroParte() {
		return numeroParte;
	}
	public void setNumeroParte(Integer numeroParte) {
		this.numeroParte = numeroParte;
	}
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	

}
