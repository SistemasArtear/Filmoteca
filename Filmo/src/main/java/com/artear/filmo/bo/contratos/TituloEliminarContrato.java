public class TituloEliminarContrato {

	private String clave;
	private String tituloCastellano;
	private String prog;
	private String confExh;
	private String aCons;
	private String rerun;

	public String getClave() {
		return clave;
	}

	public void setClave(String clave) {
		this.clave = clave;
	}

	public String getTituloCastellano() {
		return tituloCastellano;
	}

	public void setTituloCastellano(String tituloCastellano) {
		this.tituloCastellano = tituloCastellano;
	}

	public String getProg() {
		return prog;
	}

	public void setProg(String prog) {
		this.prog = prog;
	}

	public String getConfExh() {
		return confExh;
	}

	public void setConfExh(String confExh) {
		this.confExh = confExh;
	}

	public String getaCons() {
		return aCons;
	}

	public void setaCons(String aCons) {
		this.aCons = aCons;
	}

	public String getRerun() {
		return rerun;
	}

	public void setRerun(String rerun) {
		this.rerun = rerun;
	}
	
}