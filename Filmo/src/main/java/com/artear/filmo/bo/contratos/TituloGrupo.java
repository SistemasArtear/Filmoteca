package com.artear.filmo.bo.contratos;

public class TituloGrupo {

	private String clave;
	private String tituloCastellano;
	private String tituloOriginal;
	private String primerActor;
	private String segundoActor;
	private String sinContrato;

	public String getClave() {
		return clave;
	}

	public void setClave(String clave) {
		this.clave = clave;
	}

	public String getTituloCastellano() {
		return tituloCastellano;
	}
	
	public void setTituloCastellano(String tituloCastellano) {
		this.tituloCastellano = tituloCastellano;
	}
	
	public String getTituloOriginal() {
		return tituloOriginal;
	}
	
	public void setTituloOriginal(String tituloOriginal) {
		this.tituloOriginal = tituloOriginal;
	}

	public String getPrimerActor() {
		return primerActor;
	}

	public void setPrimerActor(String primerActor) {
		this.primerActor = primerActor;
	}

	public String getSegundoActor() {
		return segundoActor;
	}

	public void setSegundoActor(String segundoActor) {
		this.segundoActor = segundoActor;
	}

	public String getSinContrato() {
		return sinContrato;
	}

	public void setSinContrato(String sinContrato) {
		this.sinContrato = sinContrato;
	}
	
}