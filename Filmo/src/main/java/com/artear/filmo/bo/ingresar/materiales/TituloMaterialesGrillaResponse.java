package com.artear.filmo.bo.ingresar.materiales;

public class TituloMaterialesGrillaResponse {

	private String titulo;
	private Integer contrato;
	private Integer mas;	
	private String clave;
	private String estrenoRepeticion;
	private String formula;
	private Integer grupo;

	public String getTitulo() {
		return titulo;
	}
	
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	public Integer getContrato() {
		return contrato;
	}
	
	public void setContrato(Integer contrato) {
		this.contrato = contrato;
	}
	
	public Integer getMas() {
		return mas;
	}
	
	public void setMas(Integer mas) {
		this.mas = mas;
	}
	
	public String getClave() {
		return clave;
	}
	
	public void setClave(String clave) {
		this.clave = clave;
	}
	
	public String getEstrenoRepeticion() {
		return estrenoRepeticion;
	}
	
	public void setEstrenoRepeticion(String estrenoRepeticion) {
		this.estrenoRepeticion = estrenoRepeticion;
	}
	
	public String getFormula() {
		return formula;
	}
	
	public void setFormula(String formula) {
		this.formula = formula;
	}

	public Integer getGrupo() {
		return grupo;
	}

	public void setGrupo(Integer grupo) {
		this.grupo = grupo;
	}

}