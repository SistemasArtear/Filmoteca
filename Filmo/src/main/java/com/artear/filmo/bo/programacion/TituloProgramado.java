package com.artear.filmo.bo.programacion;

/**
 * @author sisloc
 *
 */
public class TituloProgramado {
    
    private String dia;
    private String fecha;
    private String tituloCast;
    private String clave;
    private String capitulo;
    private String parte;
    private String segmento;
    private String fraccion;
    private String totalDeFracciones;
    private String ordenSalida;
    
    public final String getDia() {
        return this.dia;
    }
    public final void setDia(String dia) {
        this.dia = dia;
    }
    public final String getFecha() {
        return this.fecha;
    }
    public final void setFecha(String fecha) {
        this.fecha = fecha;
    }
    public final String getTituloCast() {
        return this.tituloCast;
    }
    public final void setTituloCast(String tituloCast) {
        this.tituloCast = tituloCast;
    }
    public final String getClave() {
        return this.clave;
    }
    public final void setClave(String clave) {
        this.clave = clave;
    }
    public final String getCapitulo() {
        return this.capitulo;
    }
    public final void setCapitulo(String capitulo) {
        this.capitulo = capitulo;
    }
    public final String getParte() {
        return this.parte;
    }
    public final void setParte(String parte) {
        this.parte = parte;
    }
    public final String getSegmento() {
        return this.segmento;
    }
    public final void setSegmento(String segmento) {
        this.segmento = segmento;
    }
    public final String getFraccion() {
        return this.fraccion;
    }
    public final void setFraccion(String fraccion) {
        this.fraccion = fraccion;
    }
    public final String getTotalDeFracciones() {
        return this.totalDeFracciones;
    }
    public final void setTotalDeFracciones(String totalDeFracciones) {
        this.totalDeFracciones = totalDeFracciones;
    }
    public final String getOrdenSalida() {
        return this.ordenSalida;
    }
    public final void setOrdenSalida(String ordenSalida) {
        this.ordenSalida = ordenSalida;
    }

}
