package com.artear.filmo.bo.trabajar.copias;

public class TituloListado {

	private String clave;
	private String tituloOriginal;
	private String tituloCastellano;
	private String actores;
	private String tieneCopia;
	private String vigente;
	private String signo;

	public String getClave() {
		return clave;
	}

	public void setClave(String clave) {
		this.clave = clave;
	}

	public String getTituloOriginal() {
		return tituloOriginal;
	}

	public void setTituloOriginal(String tituloOriginal) {
		this.tituloOriginal = tituloOriginal;
	}

	public String getTituloCastellano() {
		return tituloCastellano;
	}

	public void setTituloCastellano(String tituloCastellano) {
		this.tituloCastellano = tituloCastellano;
	}

	public String getActores() {
		return actores;
	}

	public void setActores(String actores) {
		this.actores = actores;
	}

	public String getTieneCopia() {
		return tieneCopia;
	}

	public void setTieneCopia(String tieneCopia) {
		this.tieneCopia = tieneCopia;
	}

	public String getVigente() {
		return vigente;
	}

	public void setVigente(String vigente) {
		this.vigente = vigente;
	}

	public String getSigno() {
		return signo;
	}

	public void setSigno(String signo) {
		this.signo = signo;
	}

}
